#include "libft.h"

int	ft_strncmp(const char *s1, const char *s2, size_t n)
{
	if (n == 0)
		return (0);
	while (*s1 && *s2 && n)
	{
		if (*s1 != *s2)
			return ((unsigned char)*s1 - (unsigned char)*s2);
		s1++;
		s2++;
		n--;
	}
	if (n == 0)
		return (0);
	if ((unsigned char)*s1 < (unsigned char)*s2)
		return (-1);
	else if ((unsigned char)*s1 > (unsigned char)*s2)
		return (1);
	return (0);
}

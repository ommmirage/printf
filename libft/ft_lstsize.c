#include "libft.h"

int	ft_lstsize(t_list *lst)
{
	int		size;
	t_list	*current;

	if (!lst)
		return (0);
	size = 1;
	current = lst;
	while (current->next)
	{
		size++;
		current = current->next;
	}
	return (size);
}

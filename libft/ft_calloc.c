#include "libft.h"
#include <stdlib.h>

void	*ft_calloc(size_t nmemb, size_t size)
{
	unsigned char	*res;
	unsigned char	*res2;
	size_t			bytes;

	bytes = nmemb * size;
	if (!(res = (unsigned char *)malloc(bytes)))
		return (NULL);
	res2 = res;
	while (bytes--)
		*res++ = 0;
	return (res2);
}
